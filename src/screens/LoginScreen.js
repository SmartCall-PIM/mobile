import React, { useState } from 'react';
import {
  View,
  Text,
  TextInput,
  TouchableOpacity,
  StyleSheet,
  Alert,
  KeyboardAvoidingView,
  Platform,
  ActivityIndicator,
  Image,
} from 'react-native';
import { authService } from '../services/api';
import { useAuth } from '../context/AuthContext';

export default function LoginScreen({ navigation }) {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [loading, setLoading] = useState(false);
  const { signIn } = useAuth();

  const handleLogin = async () => {
    if (!email || !password) {
      Alert.alert('Erro', 'Por favor, preencha todos os campos');
      return;
    }

    setLoading(true);
    console.log('üîê Tentando fazer login com:', email);
    
    try {
      console.log('üì° Chamando API de login...');
      const response = await authService.login(email, password);
      console.log('‚úÖ Resposta recebida:', response);
      
      if (response.Success && response.Token) {
        console.log('‚úÖ Login bem-sucedido!');
        // Salvar token e dados do usu√°rio
        await signIn(
          {
            id: response.User.Id,
            email: response.User.Email,
            fullName: response.User.FullName,
            role: response.User.Role,
            createdAt: response.User.CreatedAt,
          },
          response.Token
        );
        
        // Navegar para a tela de cria√ß√£o de chamados
        navigation.replace('CreateTicket');
      } else {
        console.error('‚ùå Resposta sem sucesso:', response);
        Alert.alert('Erro', response.Message || 'Erro ao fazer login');
      }
    } catch (error) {
      console.error('‚ùå Erro completo:', error);
      console.error('‚ùå Erro response:', error.response);
      console.error('‚ùå Erro message:', error.message);
      
      const errorMessage = error.Message || 
                          error.Errors?.[0] ||
                          error.message ||
                          'Erro ao fazer login. Verifique suas credenciais.';
      Alert.alert('Erro de Login', errorMessage);
    } finally {
      setLoading(false);
    }
  };

  return (
    <KeyboardAvoidingView
      style={styles.container}
      behavior={Platform.OS === 'ios' ? 'padding' : 'height'}
    >
      <View style={styles.content}>
        {/* Logo/Header */}
        <View style={styles.header}>
          <Text style={styles.title}>SmartCall</Text>
          <Text style={styles.subtitle}>powered by InfinityWare</Text>
        </View>

        {/* Form */}
        <View style={styles.form}>
          <Text style={styles.label}>Email</Text>
          <TextInput
            style={styles.input}
            placeholder="Digite seu email..."
            value={email}
            onChangeText={setEmail}
            autoCapitalize="none"
            keyboardType="email-address"
            editable={!loading}
          />

          <Text style={styles.label}>Senha</Text>
          <TextInput
            style={styles.input}
            placeholder="‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢‚Ä¢"
            value={password}
            onChangeText={setPassword}
            secureTextEntry
            editable={!loading}
          />

          <TouchableOpacity
            style={[styles.button, loading && styles.buttonDisabled]}
            onPress={handleLogin}
            disabled={loading}
          >
            {loading ? (
              <ActivityIndicator color="#fff" />
            ) : (
              <Text style={styles.buttonText}>Entrar</Text>
            )}
          </TouchableOpacity>
        </View>

        {/* Footer */}
        <View style={styles.footer}>
          <Text style={styles.footerText}>
          </Text>
        </View>
      </View>
    </KeyboardAvoidingView>
  );
}

